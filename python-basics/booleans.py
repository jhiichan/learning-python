# Boolean and Conditional

# If statement
sleepy = True
if sleepy:
    print('Get Coffee!')

# If Else statement
sleepy = False
if sleepy:
    print('Get Coffee!')
else:
    print('Continue what you are doing...')

# Else If Statement
day = 'Sunday'
if day == 'Saturday':
    print('Freedom!')
elif day == 'Sunday':
    print('Shopping!')
else:
    print ('Work!')